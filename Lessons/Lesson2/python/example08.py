# Example 8 - Loops

#Print numbers from 0 to 3
n = 4

print('This is the for loop') 
for i in range(0, n): 
    print(i) 


# While loop 
print('This is the while loop') 
i = 0  # Increment variable
while ( i < n ):     
    print(i)
    i = i + 1
else:
    print('loop finished')
