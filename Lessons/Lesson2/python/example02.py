# Example 2 - Variable conversion

# Let us consider a real number converted in integer
x=-1.3
print('The number                       : ', x )
print('Converted into an intger we have : ', int(x) )

# We delete the number
del x

# Let us consider an integer converted into a real number
x=1
print('The number                       : ', x )
print('Converted into an intger we have : ', float(x) )

