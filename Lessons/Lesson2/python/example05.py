# Example 5 - Logical Operators

# Let's define two logical variables
a = True
b = False

print('If  a is ', a)
print('and b is ', b)

# The logical operators can be summarized in the following way
print('then (a and b) is      ', (a and b))
print('then (a or b) is       ', (a or b))
print('then [not (a and b)] is', not (a and b))
